CREATE TABLE Entreprise(
   id_entreprise INT,
   nom VARCHAR(50),
   secteur_activté VARCHAR(50),
   nombre_employe INT,
   nombre_de_serveur INT,
   nombre_de_postes_clients INT,
   actif TEXT,
   service_expose TEXT,
   gestion_des_vulnerabilite TEXT,
   risque_cyber TEXT,
   PRIMARY KEY(id_entreprise)
);
